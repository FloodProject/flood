namespace vapor {
namespace util {

template<class T>
class Singleton
{
protected:

	static T *Instance;

public:

	Singleton()	 { Instance = static_cast<T*>(this); }
	~Singleton() { Instance = 0; }
	
	static T &getInstance()	{ return *Instance; }
	static T *getInstancePtr()	{ return Instance; }
};

}
}